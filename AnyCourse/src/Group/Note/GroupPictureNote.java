package Group.Note;

public class GroupPictureNote {
	public int groupId;
	public int pictureNoteId;
	public int unitId;
	public String userId;
	public String pictureNoteUrl;
	public int share;
	public String shareTime;
	public int likes;
	public String schoolName;
	public String unitName;
	public String videoUrl;
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	public int getPictureNoteId() {
		return pictureNoteId;
	}
	public void setPictureNoteId(int pictureNoteId) {
		this.pictureNoteId = pictureNoteId;
	}
	public int getUnitId() {
		return unitId;
	}
	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getPictureNoteUrl() {
		return pictureNoteUrl;
	}
	public void setPictureNoteUrl(String pictureNoteUrl) {
		this.pictureNoteUrl = pictureNoteUrl;
	}
	public int getShare() {
		return share;
	}
	public void setShare(int share) {
		this.share = share;
	}
	public String getShareTime() {
		return shareTime;
	}
	public void setShareTime(String shareTime) {
		this.shareTime = shareTime;
	}
	public int getLikes() {
		return likes;
	}
	public void setLikes(int likes) {
		this.likes = likes;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getVideoUrl() {
		return videoUrl;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	
}
